public class PrimitiveType 
{
    public static void logLn(Object o){System.out.println(o);}

    public static void main(String[] args)
    {
        /**
         * PRIMITIVE TYPES HAVE WRAPPER CLASSES OBJECT TYPES
         * boolean : Boolean
         * byte: Byte
         * char: Character
         * short: Short
         * int: Integer
         * long: Long
         * float: Float
         * double: Double
         * 
         */

         int x = 4;
         logLn(x); //will give us 4

    }
}